		<p>Ph.D. Thesis &sdot; Jul 2023</p>
		<h2>
			Synthesis of Design and Operating Parameters of Photovoltaic Thermal Hybrid Collector Through
			Genetic Algorithm for Local Climate
		</h2>
		<p class="mt-6 font-bold uppercase">Abstract</p>
		<p class="mt-4">
			The concept of photovoltaic thermal (PVT) hybrid collectors has been presented and explored by
			many researchers in the past century and the technology has already been commercialized in
			European countries. The design of PVT collector for efficient operation in the tropical and
			subtropical climate of Indian subcontinent require evaluation of critical conditions and
			robust safety measures so that the reliability and integrity of crystalline silicon PV module
			is not affected. The concept of unbiased operation of PVT collector in local climate has been
			proposed for retrofitted designs in which the PVT module operates near the nominal operating
			cell temperature of PV module to keep the module warranties intact. The models of different
			complexities for simulation of PV electrical characteristics and PVT thermal performance in
			literature have been reviewed. The simulation of PVT performance in local climate require
			solar irradiance and meteorological data with good temporal resolution, for which typical
			meteorological year (TMY) dataset has been obtained for the coordinates of Pantnagar from the
			PVGIS tool that has been derived from 2005-16 time series.
		</p>
		<p>
			A methodical approach to obtain the optimum collector tilt and orientation for any location
			from its TMY data has been presented using the functions available in PVLIB toolbox to
			calculate plane-of-array irradiance. The performance of a multicrystalline PV module has been
			simulated from SAPM model using TMY data of Pantnagar. The sensitivity of TMY data variables
			on the model outputs has been assessed with several measures available in the literature to
			build confidence on the simulation results. The consolidation of state-of-the-art methods of
			performance modelling of PV and PVT collectors results in a framework, where models of
			different complexities can be chosen for the fitness function of a multi-objective genetic
			algorithm to maximize PVT operating efficiencies. Since, the electrical and thermal
			efficiencies in PVT collector conflict each other, instead of a single solution, a set of
			non-dominated Pareto optimal solutions are obtained for a PVT air heater from multi-objective
			optimization. A detailed procedure to synthesize optimum design and operating parameters of
			PVT air heater has been explained for the month of June at Pantnagar.
		</p>
